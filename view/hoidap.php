                    FAQ

Lịch sử ra đời và phát triển của G-LAB?
Thành lập vào năm 2014, với mục tiêu đem đến các sản phẩm chất lượng từ góc nhìn của những người sáng lập Cộng Đồng THẦN KINH GIÀY VIỆT NAM, G-LAB thông qua DỊCH VỤ KÝ GỬI đặc biệt đã mở rộng và phát triển thành một nền tảng để kết nối người bán và người mua ở lĩnh vực thời trang.
Với định vị độc đáo nằm ở cả Thị Trường Bán Lẻ (Retail Market) và Thị Trường Bán Lại (Resale Market), giờ đây tại G-LAB, các sản phẩm được bán không chỉ từ khách hàng, mà còn từ những nhà bán lẻ ở các thương hiệu trong và ngoài nước.

Làm sao để tôi bán sản phẩm của mình ở G-LAB?
Ở thời điểm hiện tại, để bán sản phẩm của bạn ở G-LAB, bạn phải liên hệ trước với chúng tôi thông qua các kênh liên lạc, hoặc mang sản phẩm trực tiếp đến cửa hàng để được xác định các điều kiện về sản phẩm. Sau khi các điều kiện về sản phẩm được thông qua bạn sẽ có được hợp đồng ký gởi sản phẩm tại G-LAB.

Điều kiện để sản phẩm được bán ở G-LAB ?
Không giống với việc bạn tự đăng lên mạng và bán một sản phẩm, các sản phẩm được ký gửi và bán tại G-LAB phải đảm bảo rất nhiều yếu tố như:
. Phù hợp với danh mục sản phẩm mục tiêu của G-LAB.
. Tất cả đều phải là hàng Authentic.
. Có tình trạng sản phẩm thấp nhất là 95%. Các sản phẩm có giá trị sưu tầm sẽ được quyết định tùy trường hợp.

Quy trình sơ bộ của hoạt động KÝ GỬI và bán sản phẩm tại G-LAB?
. Khi các điều kiện về sản phẩm được thông qua, G-LAB sẽ tư vấn giá bán hợp lý và thống nhất cùng bạn về các điều khoản trong hợp đồng hợp đồng ký gửi sản phẩm.
. G-LAB sẽ có trách nhiệm bán hàng của bạn tại cửa hàng vật lý và cửa hàng online trong vòng 60 ngày.
. Khi hàng bán được, G-LAB sẽ thu phí 10% giá trị hàng bán. Sau 3 ngày nếu như không có đổi trả hàng, G-LAB sẽ liên lạc với bạn để thanh toán phần tiền còn lại.
. Sau 60 ngày, trong trường hợp sản phẩm chưa bán được, chúng tôi sẽ liên hệ với bạn để tiến hành gia hạn việc ký gửi hoặc ngưng ký gửi. Trong cả 2 trường hợp, bạn sẽ không phải tốn bất kỳ chi phí nào.

Tại sao các sản phẩm cùng loại lại có giá khác nhau?
Các sản phẩm bán ở G-LAB sẽ có giá được quyết định bởi khách hàng có nhu cầu bán và ký gửi. G-LAB sẽ tư vấn về giá cả thị trường để việc bán sản phẩm có giá hợp lý. Tuy vậy, mỗi khách hàng có quyền lựa chọn giá khác nhau. Đối với các sản phẩm cùng loại và cùng tình trạng, sản phẩm có giá thấp nhất sẽ được hiển thị và bán trước.

Nếu như mua các sản phẩm đã qua sử dụng, tôi có được xem hình thực tế của sản phẩm?
Có, chắc chắn có, trước khi tiến hành việc thanh toán và giao hàng những sản phẩm đã qua sử dụng tại G-LAB, đội ngũ bán hàng sẽ liên lạc trực tiếp với bạn để gởi hình ảnh thật của sản phẩm tại thời điểm liên lạc.

Nếu như phát hiện sản phẩm không AUTHENTIC (không phải hàng thật), tôi có được trả lại sản phẩm?
Với uy tín thiết lập từ năm 2014 và với suy nghĩ đặt lợi ích của bạn lên hàng đầu, G-LAB sẽ luôn nhanh chóng giải quyết các thắc mắc của bạn về chất lượng sản phẩm. Bạn luôn có thể trả lại sản phẩm nếu như chứng minh sản phẩm mua từ G-LAB không phải là hàng thật (hàng AUTHENTIC)
